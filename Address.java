public class Address {
    //constructor that sets all values
    //getInfo() method that returns all values
    private String street;
    private String city;
    private String state;
    private int zip;

    public Address(String street, String city, String state, int zip){
        this.street = street;
        this.city = city;
        this.state = state;
        this.zip = zip;
    }

    public String getInfo(){
        return "Street number and name: " + this.street + "\n" +
                "City: " + this.city + "\n" +
                "State: " + this.state + "\n" +
                "Zip code: " + this.zip;
    }
}
